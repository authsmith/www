        default: null
    }
})

const codeCopied = ref<boolean>(false);

const copyCode = (): void => {
    navigator.clipboard.writeText(props.code)
        .then(() => {
            codeCopied.value = true;
            setTimeout(function () {
                codeCopied.value = false;
            }, 2000);
        })
        .catch((e) => {
            console.error('Error: Unable to copy code.');
        });
}
